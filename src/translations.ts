export function detectLanguage(): SupportedLanguage {
  if (typeof navigator === 'undefined') return 'en';
  const lang = navigator.language.split('-')[0].toLowerCase();
  if (lang in dictionaries) {
    return lang as SupportedLanguage;
  }
  return 'en';
}
